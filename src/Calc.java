public class Calc {
    public static void main(String[] args) {

        double a = 3.3,b = 5.5;
        calc(a,b);
    }

   public static void calc(double x, double y) {
        System.out.println(x+y);
        System.out.println(x*y);
        System.out.println(x-y);
        System.out.println(x/y);

    }
}